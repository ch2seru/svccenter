package com.wyzsoft.gen_code.model;


public class GenCode {
	private String gen_type;        /*일반코드종류*/
    private String gen_code;        /*일반코드*/
    private String description;     /*설명*/
    private String gen_value;      /*코드에 대응하는 값*/
    private String gen_value1;      /*코드에 대응하는 값*/
    private String gen_value2;      /*코드에 대응하는 값2*/
    private String active;          /*사용여부*/
	public String getGen_type() {
		return gen_type;
	}
	public void setGen_type(String gen_type) {
		this.gen_type = gen_type;
	}
	public String getGen_code() {
		return gen_code;
	}
	public void setGen_code(String gen_code) {
		this.gen_code = gen_code;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getGen_value() {
		return gen_value;
	}
	public void setGen_value(String gen_value) {
		this.gen_value = gen_value;
	}
	public String getGen_value1() {
		return gen_value1;
	}
	public void setGen_value1(String gen_value1) {
		this.gen_value1 = gen_value1;
	}
	public String getGen_value2() {
		return gen_value2;
	}
	public void setGen_value2(String gen_value2) {
		this.gen_value2 = gen_value2;
	}
	public String getActive() {
		return active;
	}
	public void setActive(String active) {
		this.active = active;
	}
    
    

}
